drop table BACKOFFICEFILE if exists;

drop table BACKOFFICEFILE_FileStatus if exists;

drop table CUSTOMERFILE if exists;

drop table CUSTOMERFILE_FileStatus if exists;

drop table EMAILNOTIFICATION if exists;

drop table FileLock if exists;

drop table FileSpecs if exists;

drop table FileStatus if exists;

drop table GftKanaal if exists;

create table BACKOFFICEFILE (
    id bigint generated by default as identity (start with 1),
    emailNotification_id bigint,
    fileSpecs_id bigint,
    primary key (id)
);

create table BACKOFFICEFILE_FileStatus (
    BACKOFFICEFILE_id bigint not null,
    history_id bigint not null,
);

create table CUSTOMERFILE (
    id bigint generated by default as identity (start with 1),
    fileSpecs_id bigint,
    gftKanaal_id bigint,
    primary key (id)
);

create table CUSTOMERFILE_FileStatus (
    CUSTOMERFILE_id bigint not null,
    history_id bigint not null
);

create table EMAILNOTIFICATION (
    id bigint generated by default as identity (start with 1),
    content varchar(10240),
    subject varchar(255),
    primary key (id)
);

create table FileLock (
    id bigint generated by default as identity (start with 1),
    lockMoment bigint,
    lockerId varchar(50),
    lockerProces varchar(50),
    fileSpecs_id bigint,
    primary key (id)
);

create table FileSpecs (
    id bigint generated by default as identity (start with 1),
    fileName varchar(255),
    filePath varchar(1024),
    fileSize bigint,
    fileLock_id bigint,
    primary key (id)
);

create table FileStatus (
    id bigint generated by default as identity (start with 1),
    moment bigint,
    status varchar(30),
    primary key (id)
);

create table GftKanaal (
    id bigint generated by default as identity (start with 1),
    kanaalNaam varchar(50),
    primary key (id)
);